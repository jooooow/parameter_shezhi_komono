void OledWriteByte(uint8_t dat)
{
    SPI4ReadWriteByte(dat);
}

void OledWriteData(uint8_t dat)
{
    CS = 0;
    DC = 1;
    OledWriteByte(dat);
}

void OledWriteCmd(uint8_t cmd)
{
    CS = 0;
		DC = 0;
    OledWriteByte(cmd);
}

